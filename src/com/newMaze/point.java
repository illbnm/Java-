package com.newMaze;

public class point {
    int i ,j;
    int di; //di为下一个可走的点

    public int getJ() {
        return j;
    }

    public void setI(int i) {
        this.i = i;
    }

    public void setDi(int di) {
        this.di = di;
    }

    public void setJ(int j) {
        this.j = j;
    }

    public int getDi() {
        return di;
    }

    public int getI() {
        return i;
    }
}
